//--------------------------------------------------------------------------------------------
/**
 *	SENoとPitchを設定して再生
 *
 *	@param	se_no	再生するSENo
 *	@param	pitch	音程変化の値（-32768〜32767）
 */
//--------------------------------------------------------------------------------------------
void	GFL_SNDTOOL_PlaySEwithPitch( int se_no, int pitch )
{
	u8	player_no;

	player_no=GFL_SOUND_GetPlayerNo(se_no);
	GFL_SOUND_PlaySE( se_no );
	GFL_SOUND_SetPitch( player_no, TRACK_MASK_FULL, pitch );
}

//--------------------------------------------------------------------------------------------
/**
 *	SENoとPanを設定して再生
 *
 *	@param	se_no	再生するSENo
 *	@param	pan		定位変化の値（-128〜127）
 */
//--------------------------------------------------------------------------------------------
void	GFL_SNDTOOL_PlaySEwithPan( int se_no, int pan )
{
	u8	player_no;

	player_no=GFL_SOUND_GetPlayerNo(se_no);
	GFL_SOUND_PlaySE( se_no );
	GFL_SOUND_SetPan( player_no, TRACK_MASK_FULL, pan );
}

//--------------------------------------------------------------------------------------------
/**
 *	SENoとPitchとPanを設定して再生
 *
 *	@param	se_no	再生するSENo
 *	@param	pitch	音程変化の値（-32768〜32767）
 *	@param	pan		定位変化の値（-128〜127）
 */
//--------------------------------------------------------------------------------------------
void	GFL_SNDTOOL_PlaySEwithPitchPan( int se_no, int pitch, int pan )
{
	u8	player_no;

	player_no=GFL_SOUND_GetPlayerNo(se_no);
	GFL_SOUND_PlaySE( se_no );
	GFL_SOUND_SetPitch( player_no, TRACK_MASK_FULL, pitch );
	GFL_SOUND_SetPan( player_no, TRACK_MASK_FULL, pan );
}


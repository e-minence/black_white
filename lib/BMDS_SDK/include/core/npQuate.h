/*!

	クォータニオンをコピーする

	@param	dst	演算結果である npFQUATE のポインタ
	@param	src	処理の基になる npFQUATE のポインタ

	@return	演算結果である npFQUATE のポインタ

*/
#define npCopyFQUATE( dst, src )	\
	( npCopyMemory( dst, src, sizeof( npFQUATE ) ), ( dst ) )

/*!

	クォータニオンの値をセットする

	@param	dst	演算結果である npFQUATE のポインタ
	@param	q0
	@param	q1
	@param	q2
	@param	q3

	@return

*/
#define	npSetFQUATE( dst, q0, q1, q2, q3 )	\
	(	( dst )->x = ( q0 ),				\
		( dst )->y = ( q1 ),				\
		( dst )->z = ( q2 ),				\
		( dst )->w = ( q3 ), ( dst ) )	


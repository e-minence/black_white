/*!

	頂点フォーマットをセットする

	@param	pContext	レンダリングコンテキスト
	@param	nFormat		頂点フォーマット

	@return	エラーコード

*/
#define	npSetVertexFormat( pContext, nFormat )	(	\
	( pContext )->m_nVertexFormat = ( nFormat ), NP_SUCCESS )

/*!

	頂点フォーマットを取得する

	@param	pContext	レンダリングコンテキスト
	@param	pFormat		問い合わせた頂点フォーマット変数の値を<br>
						格納する変数へのポインタ
	
	@return	エラーコード

*/
#define	npGetVertexFormat( pContext, pFormat )	(	\
	*( pFormat ) = ( pContext )->m_nVertexFormat, NP_SUCCESS )


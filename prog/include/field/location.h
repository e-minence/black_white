#pragma once

//--------------------------------------------------------------
///	LOCATION構造体
//--------------------------------------------------------------
typedef struct {
	int zone_id;
	s16 door_id;
	s16 dir_id;
	VecFx32 pos;
}LOCATION;

//--------------------------------------------------------------
//--------------------------------------------------------------
#define DOOR_ID_JUMP_CODE	(-1)

//=============================================================================
//=============================================================================
//--------------------------------------------------------------
//--------------------------------------------------------------
extern void LOCATION_DEBUG_SetDefaultPos(LOCATION * loc, u16 zone_id);

//--------------------------------------------------------------
/**
 * @brief	ゲーム開始時の位置取得
 */
//--------------------------------------------------------------
extern void LOCATION_SetGameStart(LOCATION * loc);

//--------------------------------------------------------------
/**
 * @brief	LOCATIONのセット
 */
//--------------------------------------------------------------
static inline void LOCATION_Set(LOCATION * loc, int zone, s16 door, s16 dir, fx32 x, fx32 y, fx32 z)
{
	loc->zone_id = zone;
	loc->door_id = door;
	loc->dir_id = dir;
	loc->pos.x = x;
	loc->pos.y = y;
	loc->pos.z = z;
}



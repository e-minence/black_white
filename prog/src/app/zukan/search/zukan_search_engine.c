/**
 *  @brief          検索する 
 *
 *  @param[in]      zkn_sv      図鑑セーブデータ
 *  @param[in]      term        検索条件
 *  @param[in]      heap_id     ヒープID
 *  @param[out]     list        現在はmonsnoの列(戻り値0のときNULL)
 *
 *  @retval         listのサイズ 
 *
 *  @note           listは呼び出し元で解放して下さい。
 *
 */
//------------------------------------------------------------------
u16 ZUKAN_SEARCH_ENGINE_Search(
               const ZUKAN_SAVEDATA*      zkn_sv,
               const ZKNCOMM_LIST_SORT*   term,
               HEAPID                     heap_id,
               u16**                      list )
{
  u16* order_list = GFL_HEAP_AllocMemory( heap_id, sizeof(u16)*MONSNO_END );
  u16 i;
  for( i=0; i<MONSNO_END; i++ )
  {
    order_list[i] = i +1;
  }
  *list = order_list;
  return MONSNO_END;
}



//============================================================================================
#pragma	once

#include "gamesystem/gamesystem.h"

//============================================================================================
//	定数定義
//============================================================================================

// セーブタイプ設定値
#define	REPORT_SAVE_TYPE_VAL	( 3 )		// セーブサイズが1/3以上ならば「たくさん書いている」と表示される

// レポート下画面ワーク
typedef struct _REPORT_WORK	REPORT_WORK;

// オーバーレイ
FS_EXTERN_OVERLAY(report);


//============================================================================================
//	プロトタイプ宣言
//============================================================================================

//--------------------------------------------------------------------------------------------
/**
 * レポート下画面初期化
 *
 * @param		gs			GAMESYS_WORK
 * @param		heapID	ヒープＩＤ
 *
 * @return	レポート下画面ワーク
 */
//--------------------------------------------------------------------------------------------
extern REPORT_WORK * REPORT_Init( GAMESYS_WORK * gs, HEAPID heapID );

//--------------------------------------------------------------------------------------------
/**
 * レポート下画面削除
 *
 * @param		wk		レポート下画面ワーク
 *
 * @return	none
 */
//--------------------------------------------------------------------------------------------
extern void REPORT_Exit( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * レポート下画面アップデート処理
 *
 * @param		wk		レポート下画面ワーク
 *
 * @return	none
 */
//--------------------------------------------------------------------------------------------
extern void REPORT_Update( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * レポート下画面描画処理
 *
 * @param		wk		レポート下画面ワーク
 *
 * @return	none
 */
//--------------------------------------------------------------------------------------------
extern void REPORT_Draw( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * 初期化終了待ち
 *
 * @param		wk		レポート下画面ワーク
 *
 * @retval	"TRUE = 初期化終了"
 * @retval	"FALSE = 初期化中"
 */
//--------------------------------------------------------------------------------------------
extern BOOL REPORT_CheckInit( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * セーブサイズ設定
 *
 * @param		wk		レポート下画面ワーク
 *
 * @return	none
 */
//--------------------------------------------------------------------------------------------
extern void REPORT_SetSaveSize( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * セーブタイプ取得（たくさん書くか）
 *
 * @param		wk		レポート下画面ワーク
 *
 * @retval	"TRUE = たくさん"
 * @retval	"FALSE = それ以外"
 */
//--------------------------------------------------------------------------------------------
extern BOOL REPORT_CheckSaveType( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * セーブ開始
 *
 * @param		wk		レポート下画面ワーク
 *
 * @return	none
 */
//--------------------------------------------------------------------------------------------
extern void REPORT_StartSave( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * セーブ終了
 *
 * @param		wk		レポート下画面ワーク
 *
 * @retval	"TRUE = 終了"
 * @retval	"FALSE = それ以外"
 */
//--------------------------------------------------------------------------------------------
extern BOOL REPORT_EndSave( REPORT_WORK * wk );

//--------------------------------------------------------------------------------------------
/**
 * セーブ強制終了
 *
 * @param		wk		レポート下画面ワーク
 *
 * @return	none
 */
//--------------------------------------------------------------------------------------------
extern void REPORT_BreakSave( REPORT_WORK * wk );
